

<h1 class="page-title mb-5">About the School</h1>
	<div class="row justify-content-center">
		<div class="col-md-10 col-12">

		<p><b>The World's Greatest Improv School</b> is a boutique improv school with classes both online and in person in Los Angeles. We focus on <I>game of the scene</i> and <i>small class sizes</i>. The name is ridiculous on purpose.</p>

		<p>Will Hines, a longtime teacher and performer who trained at the Upright Citizens Brigade Theatre in both New York and Los Angeles, founded the school in April 2020.</p>


<p>Improv classes teach listening, reacting, seeing patterns and enjoying irony. Hopefully, they'll also help you see yourself as a creative person, learn how to express your individual comedic voice, and help others to express their voice.</p>

<p>Improv has both a creative and an analytical side. It's art and math. They work together to make really interesting and surprising comedy.</p>

<p>With a small catalog of shows and jams, WGIS also wants to draw like-minded people together and let them appreciate each other's sensibilities, celebrate each other's creativity and push them to be great artists and people.</p>

<h2>Some Terms, for the Improv Aficianados</h2>

<p>If you are familiar the schools of improv out there, we teach <i>game-based</i>, <i>Chicago-style</i>, <i>long-form</i> improv with teachers mostly trained at The Upright Citizens Brigade Theatre. That all may mean nothing to you, which is fine! You are still welcome to take classes and enjoy them!</p>

<dl class="row">
  <dt class="col-sm-3">"long-form"</dt>
  <dd class="col-sm-9">This means freeform scenes that are made up on the spot, like a comedic play. This term is to distinguish it from "short-form" which are highly structured improv "games" like you might see on the show "Whose Line Is It, Anyway?" Short form is fun, but that's not what we teach here.</dd>
</dl>

<dl class="row">
  <dt class="col-sm-3">"Chicago style"</dt>
  <dd class="col-sm-9">This describes the improv teaching that descends from Chicago since the late 1950s. It focuses on mantras of "yes, and" and "truth in comedy" as developed by Del Close and other Chicago teachers. The way we get suggestions, start and end scenes, organize ourselves into teams all comes from conventions developed in Chicago. This term is used mostly in non-American improv communities to distinguish it from other big schools of improv, like the Keith Johnstone teachings.</dd>
</dl>

<dl class="row">
  <dt class="col-sm-3">"game-based"</dt>
  <dd class="col-sm-9">This describes improv that consciously focuses on finding an unusual thing and heightening it. It focuses on the comedy of the scene. This is a practice developed and championed by the Upright Citizens Brigade Theater (founders trained in Chicago, though their theaters were set up in New York City and Los Angeles).</dd>
</dl>
	
<p>Honestly, none of that matters too much. If you are interested in learning to do some improv comedy -- making up comedic scenes on the spot without a script -- we can help you do that.</p>	
		
